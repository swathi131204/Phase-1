const day = 1; 

switch (day) {
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Wednesday");
        break;
    case 4:
        console.log("Thursday");
        break;
    case 5:
        console.log("Friday");
        break;
    case 6:
        console.log("Saturday");
        console.log("It's the weekend!");
        break;
    case 7:
        console.log("Sunday");
        console.log("It's the weekend!");
        break;
    default:
        console.log("Invalid day! Please enter a number between 1 and 7.");
}
