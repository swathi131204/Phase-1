
function multiply(a, b) {
    return a * b;
}
const divide = function(a, b) {
    return a / b;
};

const power = (base, exponent) => base ** exponent;


console.log("Multiply (4 * 8):", multiply(4, 8));
console.log("Divide (66 / 4):", divide(66, 4));   
console.log("Power (4^7):", power(4, 7));        